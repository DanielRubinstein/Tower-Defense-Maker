package frontEnd.Splash;

public class StartingInput {

	
	private int tilesWide;
	private int tilesHigh;
	private double pointResolutionWidth;
	private double pointResolutionHeight;
	
	public StartingInput(){
	}

	public int getTilesWide() {
		return tilesWide;
	}

	public void setTilesWide(int tilesWide) {
		this.tilesWide = tilesWide;
	}

	public int getTilesHigh() {
		return tilesHigh;
	}

	public void setTilesHigh(int tilesHigh) {
		this.tilesHigh = tilesHigh;
	}

	public double getPointResolutionWidth() {
		return pointResolutionWidth;
	}

	public void setPointResolutionWidth(double pointResolutionWidth) {
		this.pointResolutionWidth = pointResolutionWidth;
	}

	public double getPointResolutionHeight() {
		return pointResolutionHeight;
	}

	public void setPointResolutionHeight(double pointResolutionHeight) {
		this.pointResolutionHeight = pointResolutionHeight;
	}
	
	
	
}
